package com.wonokoyo.rpawonokoyo.model;

public class ModelRencanaPanen {
    private String rit;
    private String nama_mitra;
    private String no_do;
    private String alamat;

    public ModelRencanaPanen() {
        super();
    }

    public String getRit() {
        return rit;
    }

    public void setRit(String rit) {
        this.rit = rit;
    }

    public String getNama_mitra() {
        return nama_mitra;
    }

    public void setNama_mitra(String nama_mitra) {
        this.nama_mitra = nama_mitra;
    }

    public String getNo_do() {
        return no_do;
    }

    public void setNo_do(String no_do) {
        this.no_do = no_do;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
}
